export function perspectiveNote(persp) {
  if (persp === '운영중') return `\n\n[사장님 상황] 사장님은 '사업을 운영 중'이며 자금 조달·운영 개선 관점의 상담이다. 정책자금·보증·상환 계획 중심으로 안내하고, 무리한 고금리 대출로 돌려막기 하지 않도록 주의시켜라.`;
  if (persp === '폐업·회생') return `\n\n[사장님 상황] 사장님은 '폐업을 고려 중이거나 채무가 버거운' 상황이며 정리·채무조정·재기 관점의 상담이다. 희망리턴패키지(철거비·재기), 새출발기금 채무조정, 회생·파산, 노란우산 폐업공제금 중심으로 안내하고, 연체를 방치하지 않도록 먼저 상담·신청을 권하라.`;
  return '';
}

