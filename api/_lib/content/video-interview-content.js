// 결정적 랜덤(시드) — 서버에서 매 요청 새 세션. index 기반 셔플로 Math.random 의존 최소화.
function pickQuestion(area, salt) {
  const n = area.questions.length;
  const i = Math.floor(Math.random() * n);
  return area.questions[(i + salt) % n];
}

// 6대 영역 각 1문항(랜덤) + 고정 상황질문 2문항(항상 마지막) = 기본 8문항.
export function buildSession() {
  const exp = VIDEO_INTERVIEW_AREAS.map((area, idx) => ({
    areaId: area.id,
    areaName: area.name,
    tag: area.tag,
    desc: area.desc,
    type: 'experience',
    question: pickQuestion(area, idx),
  }));
  const sit = SITUATION_QUESTIONS.map((q) => ({
    areaId: 'situation',
    areaName: '리스크 대처',
    tag: '상황질문',
    desc: '자금 위기 대처 능력',
    type: 'situation',
    sitId: q.sitId,
    question: q.question,
  }));
  return exp.concat(sit);
}

export function areaMeta() {
  return VIDEO_INTERVIEW_AREAS.map((a) => ({ id: a.id, name: a.name, tag: a.tag, desc: a.desc }));
}
